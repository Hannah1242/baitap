package Lab_2;

public class OperatorLearing {
    public static void main(String[] args) {
        int myNum1 = 1;
        int myNum2 = 2;
        // Righ operand tinh toan ben phai gan cho ben trai
        int total = myNum1 + myNum2;
        System.out.println("Total:" +total);

        //total is %d
        System.out.printf("Total is %f\n", 3.14f);
        System.out.printf("Total is %d\n", total);
        System.out.printf("Boolean value is %b", true);
        System.out.printf("Character  is %s\n", 'a');

        float myFloatNum = 3/2f;
        System.out.println(myFloatNum);

        System.out.println(5 / 2);
        System.out.println(5%2);
    }
}
